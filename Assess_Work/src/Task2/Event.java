package Task2;

public class Event {

	private String name;
	private int T_number;
	public Event(String name, int t_number) {
		super();
		this.name = name;
		T_number = t_number;
	}
	
	public String getName() {
		return name;
	}
	public int getT_number() {
		return T_number;
	}
}
